function typeWriter(element, text, i = 0) {
  if (i < text.length) {
    element.innerHTML += text.charAt(i);
    setTimeout(() => typeWriter(element, text, i + 1), 25);
  }
}

function showSongs(mood) {
  const container = document.getElementById("cardSection");
  container.innerHTML = "";

  songs[mood].forEach(song => {
    const card = document.createElement("div");
    card.className = "card";
    card.onclick = () => window.open(song.video, "_blank");

    const img = document.createElement("img");
    img.src = song.thumbnail;
    img.alt = song.title;
    img.className = "thumb";
    card.appendChild(img);

    const title = document.createElement("h3");
    card.appendChild(title);
    typeWriter(title, song.title);

    container.appendChild(card);
  });
}
